# reversing a string or a number
use strict;
use warnings;
use 5.022;

my $string="headache";
print $string;

print "\n";

# reversing the $string
my $reverse_str=reverse $string;
print $reverse_str;

print "\n";

my $number=3.14;
print $number;

print "\n";

# reversing the number
my $reverse_num=reverse $number;
print $reverse_num;
